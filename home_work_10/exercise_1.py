# Опис завдання
'''
Завдання 1. Оптимізація виробництва

Компанія виробляє два види напоїв: "Лимонад" і "Фруктовий сік". Для виробництва цих напоїв використовуються різні 
інгредієнти та обмежена кількість обладнання. Задача полягає у максимізації виробництва, враховуючи обмежені ресурси.

Умови завдання:

1. "Лимонад" виготовляється з "Води", "Цукру" та "Лимонного соку".
2. "Фруктовий сік" виготовляється з "Фруктового пюре" та "Води".
3. Обмеження ресурсів: 100 од. "Води", 50 од. "Цукру", 30 од. "Лимонного соку" та 40 од. "Фруктового пюре".
4. Виробництво одиниці "Лимонаду" вимагає 2 од. "Води", 1 од. "Цукру" та 1 од. "Лимонного соку".
5. Виробництво одиниці "Фруктового соку" вимагає 2 од. "Фруктового пюре" та 1 од. "Води".

Використовуючи PuLP, створіть модель, яка визначає, скільки "Лимонаду" та "Фруктового соку" потрібно 
виробити для максимізації загальної кількості продуктів, дотримуючись обмежень на ресурси. Напишіть 
програму, код якої максимізує загальну кількість вироблених продуктів "Лимонад" та "Фруктовий сік", 
враховуючи обмеження на кількість ресурсів.


'''
# Виконання завдання
import pulp

# Створення моделі
model = pulp.LpProblem("Maximize_Production", pulp.LpMaximize)

# Змінні (кількість лимонаду та соку)
x = pulp.LpVariable("Lemonade", lowBound=0, cat='Integer')
y = pulp.LpVariable("Fruit_Juice", lowBound=0, cat='Integer')

# Цільова функція: максимізувати загальну кількість напоїв
model += x + y, "Total_Production"

# Обмеження ресурсів
model += 2 * x + 1 * y <= 100, "Water"
model += 1 * x <= 50, "Sugar"
model += 1 * x <= 30, "Lemon_Juice"
model += 2 * y <= 40, "Fruit_Puree"

# Розв'язання моделі
model.solve()

# Виведення результатів
print("Status:", pulp.LpStatus[model.status])
print("Кількість лимонаду:", int(x.value()))
print("Кількість фруктового соку:", int(y.value()))
print("Загальна кількість напоїв:", int(x.value() + y.value()))
