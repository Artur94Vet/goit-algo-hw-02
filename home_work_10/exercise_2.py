'''
–ó–∞–≤–¥–∞–Ω–Ω—è 2. –û–±—á–∏—Å–ª–µ–Ω–Ω—è –≤–∏–∑–Ω–∞—á–µ–Ω–æ–≥–æ —ñ–Ω—Ç–µ–≥—Ä–∞–ª–∞

–í–∞—à–µ –¥—Ä—É–≥–µ –∑–∞–≤–¥–∞–Ω–Ω—è –ø–æ–ª—è–≥–∞—î –≤ –æ–±—á–∏—Å–ª–µ–Ω–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è —ñ–Ω—Ç–µ–≥—Ä–∞–ª–∞ —Ñ—É–Ω–∫—Ü—ñ—ó –º–µ—Ç–æ–¥–æ–º –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ.

 üìñ –ú–æ–∂–µ—Ç–µ –æ–±—Ä–∞—Ç–∏ —Ñ—É–Ω–∫—Ü—ñ—é –Ω–∞ –≤–ª–∞—Å–Ω–∏–π —Ä–æ–∑—Å—É–¥.
–í–∏–∫–æ–Ω–∞—î–º–æ –ø–æ–±—É–¥–æ–≤—É –≥—Ä–∞—Ñ—ñ–∫–∞.



‚úÇÔ∏è –¶–µ –º–æ–∂–Ω–∞ –∑–∞–ø—É—Å—Ç–∏—Ç–∏!

import matplotlib.pyplot as plt
import numpy as np

# –í–∏–∑–Ω–∞—á–µ–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó —Ç–∞ –º–µ–∂—ñ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è
def f(x):
    return x ** 2

a = 0  # –ù–∏–∂–Ω—è –º–µ–∂–∞
b = 2  # –í–µ—Ä—Ö–Ω—è –º–µ–∂–∞

# –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –¥—ñ–∞–ø–∞–∑–æ–Ω—É –∑–Ω–∞—á–µ–Ω—å –¥–ª—è x
x = np.linspace(-0.5, 2.5, 400)
y = f(x)

# –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –≥—Ä–∞—Ñ—ñ–∫–∞
fig, ax = plt.subplots()

# –ú–∞–ª—é–≤–∞–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó
ax.plot(x, y, 'r', linewidth=2)

# –ó–∞–ø–æ–≤–Ω–µ–Ω–Ω—è –æ–±–ª–∞—Å—Ç—ñ –ø—ñ–¥ –∫—Ä–∏–≤–æ—é
ix = np.linspace(a, b)
iy = f(ix)
ax.fill_between(ix, iy, color='gray', alpha=0.3)

# –ù–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è –≥—Ä–∞—Ñ—ñ–∫–∞
ax.set_xlim([x[0], x[-1]])
ax.set_ylim([0, max(y) + 0.1])
ax.set_xlabel('x')
ax.set_ylabel('f(x)')

# –î–æ–¥–∞–≤–∞–Ω–Ω—è –º–µ–∂ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è —Ç–∞ –Ω–∞–∑–≤–∏ –≥—Ä–∞—Ñ—ñ–∫–∞
ax.axvline(x=a, color='gray', linestyle='--')
ax.axvline(x=b, color='gray', linestyle='--')
ax.set_title('–ì—Ä–∞—Ñ—ñ–∫ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è f(x) = x^2 –≤—ñ–¥ ' + str(a) + ' –¥–æ ' + str(b))
plt.grid()
plt.show()

1. –û–±—á–∏—Å–ª—ñ—Ç—å –∑–Ω–∞—á–µ–Ω–Ω—è —ñ–Ω—Ç–µ–≥—Ä–∞–ª–∞ —Ñ—É–Ω–∫—Ü—ñ—ó –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é –º–µ—Ç–æ–¥—É –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ, —ñ–Ω–∞–∫—à–µ –∫–∞–∂—É—á–∏, –∑–Ω–∞–π–¥—ñ—Ç—å –ø–ª–æ—â—É –ø—ñ–¥ —Ü–∏–º –≥—Ä–∞—Ñ—ñ–∫–æ–º (—Å—ñ—Ä–∞ –∑–æ–Ω–∞).

2. –ü–µ—Ä–µ–≤—ñ—Ä—Ç–µ –ø—Ä–∞–≤–∏–ª—å–Ω—ñ—Å—Ç—å —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—ñ–≤, —â–æ–± –ø—ñ–¥—Ç–≤–µ—Ä–¥–∏—Ç–∏ —Ç–æ—á–Ω—ñ—Å—Ç—å –º–µ—Ç–æ–¥—É –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ, —à–ª—è—Ö–æ–º –ø–æ—Ä—ñ–≤–Ω—è–Ω–Ω—è –æ—Ç—Ä–∏–º–∞–Ω–æ–≥–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—É —Ç–∞ –∞–Ω–∞–ª—ñ—Ç–∏—á–Ω–∏—Ö 
—Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—ñ–≤ –∞–±–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—É –≤–∏–∫–æ–Ω–∞–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó quad. –ó—Ä–æ–±—ñ—Ç—å –≤–∏—Å–Ω–æ–≤–∫–∏.

'''
import numpy as np
import matplotlib.pyplot as plt
import scipy.integrate as spi

# --- 1. –í–∏–∑–Ω–∞—á–µ–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó ---
def f(x):
    return x ** 2

# --- 2. –ú–µ–∂—ñ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è ---
a = 0
b = 2

# --- 3. –ü–æ–±—É–¥–æ–≤–∞ –≥—Ä–∞—Ñ—ñ–∫–∞ —Ñ—É–Ω–∫—Ü—ñ—ó ---
x_vals = np.linspace(-0.5, 2.5, 400)
y_vals = f(x_vals)

fig, ax = plt.subplots()
ax.plot(x_vals, y_vals, 'r', linewidth=2)

# –ó–∞–ø–æ–≤–Ω–µ–Ω–Ω—è –æ–±–ª–∞—Å—Ç—ñ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è
ix = np.linspace(a, b, 100)
iy = f(ix)
ax.fill_between(ix, iy, color='gray', alpha=0.3)

# –í—ñ—Å—å, –º—ñ—Ç–∫–∏, –º–µ–∂—ñ
ax.axvline(x=a, color='gray', linestyle='--')
ax.axvline(x=b, color='gray', linestyle='--')
ax.set_xlim([x_vals[0], x_vals[-1]])
ax.set_ylim([0, max(y_vals) + 0.1])
ax.set_xlabel('x')
ax.set_ylabel('f(x)')
ax.set_title('–ì—Ä–∞—Ñ—ñ–∫ f(x) = x¬≤ –Ω–∞ –≤—ñ–¥—Ä—ñ–∑–∫—É [0, 2]')
plt.grid()
plt.show()

# --- 4. –ú–µ—Ç–æ–¥ –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ ---
N = 100000  # –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Ç–æ—á–æ–∫
x_rand = np.random.uniform(a, b, N)
fx_rand = f(x_rand)
mean_fx = np.mean(fx_rand)
mc_integral = (b - a) * mean_fx

print("–Ü–Ω—Ç–µ–≥—Ä–∞–ª –º–µ—Ç–æ–¥–æ–º –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ:", mc_integral)

# --- 5. –¢–æ—á–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è —ñ–Ω—Ç–µ–≥—Ä–∞–ª—É —á–µ—Ä–µ–∑ quad ---
exact_result, error_est = spi.quad(f, a, b)

print("–ê–Ω–∞–ª—ñ—Ç–∏—á–Ω–∏–π —ñ–Ω—Ç–µ–≥—Ä–∞–ª (quad):", exact_result)
print("–ê–±—Å–æ–ª—é—Ç–Ω–∞ –ø–æ—Ö–∏–±–∫–∞:", abs(exact_result - mc_integral))
print("–û—Ü—ñ–Ω–∫–∞ –ø–æ–º–∏–ª–∫–∏ quad:", error_est)
