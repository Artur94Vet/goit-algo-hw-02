# Опис завдання
'''
Завдання 1
Потрібно розробити програму, яка імітує приймання й обробку заявок: програма має автоматично генерувати нові заявки (ідентифіковані унікальним номером або іншими даними), 
додавати їх до черги, а потім послідовно видаляти з черги для "обробки", імітуючи таким чином роботу сервісного центру.

Ось псевдокод для завдання з використанням черги (Queue з модуля queue в Python) для системи обробки заявок:
-----------------------------------------
import Queue

Створити чергу заявок
queue = Queue()

Функція generate_request():
    Створити нову заявку
    Додати заявку до черги

Функція process_request():
    Якщо черга не пуста:
        Видалити заявку з черги
        Обробити заявку
    Інакше:
        Вивести повідомлення, що черга пуста

Головний цикл програми:
    Поки користувач не вийде з програми:
        Виконати generate_request() для створення нових заявок
        Виконати process_request() для обробки заявок
-----------------------------------------
У цьому псевдокоді використовуються дві основні функції: generate_request(), яка генерує нові заявки та додає їх до черги, та process_request(), 
яка обробляє заявки, видаляючи їх із черги. Головний цикл програми виконує ці функції, імітуючи постійний потік нових заявок та їх обробку.
'''
# Виконання завдання
import queue
import time
import random

# Створити чергу заявок
request_queue = queue.Queue()

# Функція генерації нової заявки
def generate_request():
    request_id = random.randint(100, 999)  # Генеруємо випадковий номер заявки
    request_data = f"Заявка №{request_id}"
    request_queue.put(request_data)
    print(f"Згенеровано нову заявку: {request_data}")
    time.sleep(random.uniform(0.1, 0.9))
# Функція обробки заявки
def process_request():
    if not request_queue.empty():
        request_data = request_queue.get()
        print(f"Обробка заявки: {request_data}")
        time.sleep(random.uniform(0.5, 0.6))  # Імітація часу обробки
    else:
        print("Черга пуста. Немає заявок для обробки.")

# Головний цикл програми
def main():
    try:
        while True:
            generate_request()  # Завжди генеруємо нову заявку
            process_request()   # Завжди обробляємо одну заявку
            time.sleep(1)       # Невелика пауза для симуляції
    except KeyboardInterrupt:
        print("\nРоботу програми перервано користувачем.")

if __name__ == "__main__":
    main()


